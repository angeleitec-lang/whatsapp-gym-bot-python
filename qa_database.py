def get_qa_database():
    """Return the complete Q&A database."""
    return QA_DATABASE

def search_qa(query: str) -> list:
    """
    Search the Q&A database for matching questions.
    
    Args:
        query: Search query string
        
    Returns:
        List of matching Q&A pairs
    """
    query_lower = query.lower()
    results = []
    
    for category, items in QA_DATABASE.items():
        for item in items:
            # Search in question
            if query_lower in item['question'].lower():
                results.append(item)
                continue
            
            # Search in keywords
            for keyword in item['keywords']:
                if query_lower in keyword.lower():
                    results.append(item)
                    break
    
    return results

def get_random_questions(count: int = 5) -> list:
    """Get random questions from the database."""
    import random
    all_items = []
    
    for category, items in QA_DATABASE.items():
        all_items.extend(items)
    
    return random.sample(all_items, min(count, len(all_items)))
